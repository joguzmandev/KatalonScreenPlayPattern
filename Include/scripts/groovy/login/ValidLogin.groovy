class ValidLogin {


	@Given("I want to sign in to go home page")
	def i_want_to_sign_in_togo_home_page() {
		Navigate.toUrl(GlobalVariable.url_site)
	}

	@When("I type username and password with rol (.*)")
	def i_type_username_and_password(rolType) {
		def data = FetchUserFactory.getUserByRol(rolType)
		String username = data["username"]
		String password = data["password"]

		LoginTasks login = new LoginTasks()
		login.doLogin(username, password)
	}

	@Then("I should be able to access home page")
	def i_should_be_able_to_access_home_page() {
		LoginQuestions loginQuestions = new LoginQuestions()
		loginQuestions.isLoginSuccess()
	}
}